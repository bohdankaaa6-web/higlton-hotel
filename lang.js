document.addEventListener('DOMContentLoaded', () => {
    const langButtons = document.querySelectorAll('.lang-btn');
    let currentLang = localStorage.getItem('site_language') || 'en';

    function setLanguage(lang) {
        // Шукаємо всі елементи, які треба перекласти (звичайний текст і плейсхолдери)
        const elementsToTranslate = document.querySelectorAll('[data-i18n], [data-i18n-placeholder]');
        
        elementsToTranslate.forEach(element => {
            // 1. Переклад звичайного тексту
            if (element.hasAttribute('data-i18n')) {
                const key = element.getAttribute('data-i18n');
                if (translations[lang] && translations[lang][key]) {
                    element.innerHTML = translations[lang][key]; 
                }
            }
            
            // 2. Переклад плейсхолдерів (тексту в полях вводу)
            if (element.hasAttribute('data-i18n-placeholder')) {
                const key = element.getAttribute('data-i18n-placeholder');
                if (translations[lang] && translations[lang][key]) {
                    element.placeholder = translations[lang][key]; 
                }
            }
        });

        // Оновлюємо підсвітку кнопок мови (EN | UA)
        langButtons.forEach(btn => {
            if (btn.getAttribute('data-lang') === lang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });

        // Зберігаємо вибір користувача у пам'яті браузера
        localStorage.setItem('site_language', lang);
    }

    // Запускаємо мову при завантаженні сторінки
    setLanguage(currentLang);

    // Додаємо подію кліку на кнопки перемикання мови
    langButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            const selectedLang = btn.getAttribute('data-lang');
            setLanguage(selectedLang);
        });
    });
});







